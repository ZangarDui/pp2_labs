import math  # 1. Математикалық операциялар үшін math модулін импорттаймыз

class Point:  # 2. Point деген класс анықтаймыз
    def __init__(self, x, y):  # 3. Конструктор, `x` және `y` координаттарын алады
        self.x = x  # 4. Нүктенің X координаттарын сақтайтын айнымалы
        self.y = y  # 5. Нүктенің Y координаттарын сақтайтын айнымалы

    def show(self):  # 6. `show` әдісі: Нүктенің координаттарын көрсету
        print(f"Point: ({self.x}, {self.y})")  # 7. Координаттарды экранға шығару

    def move(self, dx, dy):  # 8. `move` әдісі: Нүктенің координаттарын өзгерту
        self.x += dx  # 9. X координатын өзгерту
        self.y += dy  # 10. Y координатын өзгерту

    def dist(self, other_point):  # 11. `dist` әдісі: екі нүкте арасындағы қашықтықты есептеу
        distance = math.sqrt((self.x - other_point.x) ** 2 + (self.y - other_point.y) ** 2)  # 12. Екі нүкте арасындағы қашықтықты есептеу
        return distance  # 13. Қашықтықты қайтарады

# Мысал қолдану
point1 = Point(3, 4)  # 14. `Point` объектісін жасау (x=3, y=4)
point2 = Point(6, 8)  # 15. Басқа бір `Point` объектісін жасау (x=6, y=8)

point1.show()  # 16. Нүкте 1-дің координаттарын көрсету
point2.show()  # 17. Нүкте 2-дің координаттарын көрсету

# Нүкте 1-ді жылжыту (dx=2, dy=3)
point1.move(2, 3)
point1.show()  # 18. Жылжытылған Нүкте 1-дің координаттарын көрсету

# Екі нүкте арасындағы қашықтықты есептеу
distance = point1.dist(point2)  # 19. `dist` әдісін шақыру арқылы қашықтықты есептеу
print(f"Қашықтық: {distance}")  # 20. Қашықтықты экранға шығару
